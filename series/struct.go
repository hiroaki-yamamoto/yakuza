package series

// Series represents a simple alias of the array of interface
type Series []interface{}

// IntSeries represents a simple alias of the array of int
type IntSeries []int

// Int16Series represents a simple alias of the array of int16
type Int16Series []int16

// Int32Series represents a simple alias of the array of int32
type Int32Series []int16

// Int64Series represents a simple alias of the array of int64
type Int64Series []int16

// Float32Series represents a simple alias of the array of float32
type Float32Series []int16

// Float64Series represents a simple alias of the array of float64
type Float64Series []int16

// MapSeries represents a simple map with string key and interface value.
type MapSeries map[string]interface{}

// MapIntSeries represents a simple map with string key and int value.
type MapIntSeries map[string]int

// MapInt16Series represents a simple map with string key and int16 value.
type MapInt16Series map[string]int16

// MapInt32Series represents a simple map with string key and int32 value.
type MapInt32Series map[string]int32

// MapInt64Series represents a simple map with string key and int64 value.
type MapInt64Series map[string]int64

// MapFloat32Series represents a simple map with string key and float32 value.
type MapFloat32Series map[string]float32

// MapFloat64Series represents a simple map with string key and float64 value.
type MapFloat64Series map[string]float64
